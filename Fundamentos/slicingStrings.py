Var = "Gustavo" 
#      0123456

print(Var[3])
print('--------')

print(Var[0 : 3]) # da posicao 0 ate a 3.
print('--------')

print(Var[3 : ]) # da posicao 3 ate o fim.
print('--------')

print(Var[ : 5 ]) # ate a posicao 5.
print('--------')

print(Var[-2]) # de tras para frente
print('--------')

print(Var[-3 : ]) 
print('--------')

print(Var[ : -3])
print('--------')

print(Var[-5 : -3])
print('--------')

print(Var[: : -1]) # invertido
print('--------')

print(Var[4 : : -1]) # da posicao 4 invertido
print('--------')

print(Var[5 : 2 : -1]) # da 5 ate a 2 invertido
print('--------')